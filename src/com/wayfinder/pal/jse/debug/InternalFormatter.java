package com.wayfinder.pal.jse.debug;

import java.io.PrintWriter;
import java.io.StringWriter;
import java.text.SimpleDateFormat;
import java.util.Date;
import java.util.logging.Formatter;
import java.util.logging.LogRecord;

class InternalFormatter extends Formatter {

    private final Date dat;
    private final SimpleDateFormat dateFormatter;

    // Line separator string.
    private String lineSeparator = System.getProperty("line.separator");

    private final boolean m_printTime;

    InternalFormatter(boolean printTime) {
        dat = new Date();
        m_printTime = printTime;
        if(printTime) {
            // format time the same way as the XML server
            dateFormatter = new SimpleDateFormat("yy-MM-dd HH:mm:ss");
        } else {
            dateFormatter = null;
        }
    }


    /**
     * Format the given LogRecord.
     * @param record the log record to be formatted.
     * @return a formatted log record
     */
    public synchronized String format(LogRecord record) {
        StringBuffer sb = new StringBuffer();
        if(m_printTime) {
            // format same format as xml server :P
            // Minimize memory allocations here.
            dat.setTime(record.getMillis());
            sb.append(dateFormatter.format(dat));
            sb.append(" ");
        }
        
        String message = formatMessage(record);
        sb.append(record.getLevel().getLocalizedName());
        sb.append(": ");
        sb.append(message);
        sb.append(lineSeparator);
        if (record.getThrown() != null) {
            try {
                StringWriter sw = new StringWriter();
                PrintWriter pw = new PrintWriter(sw);
                record.getThrown().printStackTrace(pw);
                pw.close();
                sb.append(sw.toString());
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
        return sb.toString();
    }
    
}
